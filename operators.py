def sum (number1, number2):
  return number1 + number2

def sub (number1, number2):
  return number1 - number2

def mult (number1, number2):
  return number1 * number2

def potencial (number1, number2):
  return number1 ** number2

def div (number1, number2):
  return number1 / number2

print(sum(2,2))
print(sub(2,2))
print(mult(2,2))
print(div(2,2))
print(potencial(2,2))